#include "libft.h"

int		ft_cwords(char *s, char del)
{
	int		wc;
	size_t	i;

	wc = 0;
	i = -1;
	while (s[++i] && i <= ft_strlen(s))
		if (s[i] != del)
		{
			while (s[i] != del && i++)
				;
			wc++;
		}
	return (wc);
}
